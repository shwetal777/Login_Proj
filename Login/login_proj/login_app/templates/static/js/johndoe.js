//Check to see if the window is top if not then display button
$(window).scroll(function() {
  if ($(this).scrollTop() > 100) {
    $('#toTopBtn').fadeIn();
  }
  else {
      $('#toTopBtn').fadeOut();
  }
});

//Click event to scroll to top
$('#toTopBtn').click(function() {
  $("html, body").animate({
    scrollTop: 0
  }, 1500);
  return false;
});

